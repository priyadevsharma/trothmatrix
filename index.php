$("#sendMail").on("submit",function(e)
{
	e.preventDefault();
	
	var obj = $(this), action_name = obj.attr('name'); 
	$("#submit_btn").attr("disabled", true);
	$("#submit_btn").html("Please Wait...");
	
	$.ajax
	({
		type: "POST",
		url: e.target.action,
		data: obj.serialize()+"&tag="+action_name,
		cache: false,
		success: function (response) 
		{
            console.log(response);
			if(response.status == 0)
			{
				Swal.fire({
                    position: 'center',
                    icon: 'error',
                    title: response.message,
                    showConfirmButton: false,
                    timer: 1500
                })

				$("#submit_btn").html('Send');
				$("#submit_btn").removeAttr('disabled');
				return false;
			}
			else
			{    
                Swal.fire({
                    position: 'center',
                    icon: 'success',
                    title: response.message,
                    showConfirmButton: false,
                    timer: 1500
                })
				$("#submit_btn").html('Send');
				setTimeout(function()
				{
					location.reload(true);
				}, 2000);
			}
		}
	});
	
});

</script>